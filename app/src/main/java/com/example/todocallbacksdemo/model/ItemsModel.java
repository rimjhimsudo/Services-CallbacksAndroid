package com.example.todocallbacksdemo.model;

public class ItemsModel {
    String name;
    int num;

    public ItemsModel(String name, int num) {
        this.name = name;
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
